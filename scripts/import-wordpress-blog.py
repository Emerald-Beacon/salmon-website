def update_blog_index(posts: List[dict]) -> None:
    html_text = BLOG_INDEX_PATH.read_text("utf-8")
    cards_markup = "\n\n".join(render_blog_card(post) for post in posts)
    replacement = f"""<!-- Blog Grid -->
<main class="container" style="padding-top:3rem;padding-bottom:4rem;">

  <div class="blog-grid">

{cards_markup}

  </div>
</main>"""
    updated = re.sub(
        r'<!-- Blog Grid -->[\s\S]*?</main>',
        replacement,
        html_text,
        count=1,
    )
    BLOG_INDEX_PATH.write_text(updated, "utf-8")


def normalize_imported_post(
    raw_post: dict,
    postmeta: Dict[int, Dict[str, List[object]]],
) -> Optional[dict]:
    slug = raw_post["post_name"].strip()
    if not slug:
        return None

    attachment_values = postmeta.get(int(raw_post["ID"]), {}).get("_thumbnail_id", [])
    attachment_id = int(attachment_values[0]) if attachment_values else None
    image_relative_path = wp_image_path_from_attachment(attachment_id, postmeta)
    image_url = copy_featured_image(image_relative_path)

    content_html = clean_html_content(raw_post["post_content"])
    excerpt = clean_text(raw_post["post_excerpt"]) or excerpt_from_content(content_html)
    if not excerpt:
        excerpt = clean_text(raw_post["post_title"])

    title = clean_text(raw_post["post_title"])
    image_alt = wp_image_alt(attachment_id, postmeta, title)
    published = iso_date(raw_post["post_date"])
    modified = iso_date(raw_post["post_modified"] or raw_post["post_date"])
    read_time = estimate_read_time(content_html)
    hero_subtext = excerpt

    return {
        "slug": slug,
        "title": title,
        "excerpt": excerpt,
        "content": content_html,
        "heroSubtext": hero_subtext,
        "image": image_url,
        "imageAlt": image_alt,
        "date": published,
        "modified": modified,
        "dateDisplay": human_date(published),
        "readTime": read_time,
        "url": f"/blog/{slug}/",
    }


def write_imported_post(post: dict) -> None:
    post_dir = BLOG_ROOT / post["slug"]
    post_dir.mkdir(parents=True, exist_ok=True)
    (post_dir / "index.html").write_text(render_post_html(post), "utf-8")


def sort_posts(posts: Iterable[dict]) -> List[dict]:
    return sorted(posts, key=lambda entry: (entry["date"], entry["title"].lower()), reverse=True)


def build_posts_json_entry(post: dict) -> dict:
    return {
        "title": post["title"],
        "excerpt": post["excerpt"],
        "url": post["url"],
        "image": post["image"] or "/assets/images/logo-salmon.png",
        "imageAlt": post["imageAlt"],
        "date": post["date"],
        "dateDisplay": post["dateDisplay"],
        "readTime": post["readTime"],
    }


def main() -> None:
    if not SQL_PATH.exists():
        raise SystemExit(f"Missing SQL dump at {SQL_PATH}")

    posts, postmeta = load_wordpress_data()
    authoritative_slugs = current_authoritative_slugs()
    current_posts = load_current_posts()

    published_wp_posts = [
        post
        for post in posts
        if post["post_type"] == "post" and post["post_status"] == "publish"
    ]

    imported_posts = []
    for raw_post in published_wp_posts:
        normalized = normalize_imported_post(raw_post, postmeta)
        if not normalized:
            continue
        if normalized["slug"] in authoritative_slugs:
            continue
        imported_posts.append(normalized)

    imported_posts = sort_posts(imported_posts)

    for post in imported_posts:
        write_imported_post(post)

    current_post_entries = []
    for slug, entry in current_posts.items():
        if slug not in authoritative_slugs:
            continue
        current_post_entries.append(
            {
                "slug": slug,
                "title": entry["title"],
                "excerpt": entry["excerpt"],
                "image": entry.get("image"),
                "imageAlt": entry.get("imageAlt") or entry["title"],
                "date": entry["date"],
                "dateDisplay": entry["dateDisplay"],
                "readTime": entry["readTime"],
                "url": entry["url"],
            }
        )

    merged_posts = sort_posts([*current_post_entries, *imported_posts])
    POSTS_JSON_PATH.write_text(
        json.dumps([build_posts_json_entry(post) for post in merged_posts], indent=2) + "\n",
        "utf-8",
    )
    update_blog_index(merged_posts)

    print(
        json.dumps(
            {
                "published_wp_posts": len(published_wp_posts),
                "imported_posts": len(imported_posts),
                "existing_authoritative_posts": len(authoritative_slugs),
                "merged_posts": len(merged_posts),
            },
            indent=2,
        )
    )


if __name__ == "__main__":
    main()
