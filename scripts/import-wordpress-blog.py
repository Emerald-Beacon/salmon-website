def split_sql_tuples(values_blob: str) -> List[List[str]]:
    tuples: List[List[str]] = []
    i = 0
    length = len(values_blob)

    while i < length:
        if values_blob[i] != "(":
            i += 1
            continue

        i += 1
        current = []
        field: List[str] = []
        in_string = False
        escaped = False

        while i < length:
            char = values_blob[i]

            if in_string:
                field.append(char)
                if escaped:
                    escaped = False
                elif char == "\\":
                    escaped = True
                elif char == "'":
                    in_string = False
                i += 1
                continue

            if char == "'":
                in_string = True
                field.append(char)
                i += 1
                continue

            if char == ",":
                current.append("".join(field).strip())
                field = []
                i += 1
                continue

            if char == ")":
                current.append("".join(field).strip())
                tuples.append(current)
                i += 1
                break

            field.append(char)
            i += 1

        while i < length and values_blob[i] in ", \n\r\t":
            i += 1

    return tuples


def parse_sql_value(raw: str):
    if raw == "NULL":
        return None

    if raw.startswith("'") and raw.endswith("'"):
        value = raw[1:-1]
        value = (
            value.replace("\\\\", "\\")
            .replace("\\'", "'")
            .replace('\\"', '"')
            .replace("\\/", "/")
            .replace("\\n", "\n")
            .replace("\\r", "\r")
            .replace("\\0", "\x00")
        )
        return value

    try:
        return int(raw)
    except ValueError:
        return raw


def parse_insert_rows(table_name: str, text: str) -> Iterable[List[object]]:
    pattern = re.compile(rf"INSERT INTO `{table_name}` VALUES\s*(.+?);\n", re.S)
    for match in pattern.finditer(text):
        for row in split_sql_tuples(match.group(1)):
            yield [parse_sql_value(value) for value in row]


def load_wordpress_data() -> tuple[List[dict], Dict[int, Dict[str, List[object]]]]:
    sql = SQL_PATH.read_text("utf-8", errors="replace")

    posts = []
    for row in parse_insert_rows("SERVMASK_PREFIX_posts", sql):
        if len(row) < 23:
            continue
        posts.append(
            {
                "ID": row[0],
                "post_author": row[1],
                "post_date": row[2],
                "post_date_gmt": row[3],
                "post_content": row[4] or "",
                "post_title": row[5] or "",
                "post_excerpt": row[6] or "",
                "post_status": row[7],
                "comment_status": row[8],
                "ping_status": row[9],
                "post_name": row[11] or "",
                "post_modified": row[14],
                "post_modified_gmt": row[15],
                "guid": row[18] or "",
                "post_type": row[20],
                "post_parent": row[21],
            }
        )

    postmeta: Dict[int, Dict[str, List[object]]] = {}
    for row in parse_insert_rows("SERVMASK_PREFIX_postmeta", sql):
        if len(row) < 4:
            continue
        post_id = int(row[1])
        meta_key = row[2]
        meta_value = row[3]
        postmeta.setdefault(post_id, {}).setdefault(meta_key, []).append(meta_value)

    return posts, postmeta


def clean_text(value: str) -> str:
    text = re.sub(r"<[^>]+>", " ", value or "")
    text = html.unescape(text)
    text = re.sub(r"\s+", " ", text).strip()
    text = re.sub(r"\s+([,.;:!?])", r"\1", text)
    return text


def clean_html_content(content: str) -> str:
    cleaned = content or ""
    cleaned = re.sub(r"<!--\s*wp:[\s\S]*?-->", "", cleaned)
    cleaned = re.sub(r"<!--\s*/wp:[\s\S]*?-->", "", cleaned)
    cleaned = re.sub(r"\[/?caption[^\]]*\]", "", cleaned)
    cleaned = re.sub(r"<script[\s\S]*?</script>", "", cleaned, flags=re.I)
    cleaned = re.sub(r"<style[\s\S]*?</style>", "", cleaned, flags=re.I)
    cleaned = re.sub(r'\s+class="[^"]*"', "", cleaned)
    cleaned = re.sub(r'\s+id="[^"]*"', "", cleaned)
    cleaned = re.sub(r"\s+data-[a-zA-Z0-9_-]+=\"[^\"]*\"", "", cleaned)
    cleaned = re.sub(r"\s+aria-label=\"[^\"]*\"", "", cleaned)
    cleaned = cleaned.replace("\r", "")
    cleaned = rewrite_site_urls(cleaned)
    cleaned = re.sub(r"\n{3,}", "\n\n", cleaned).strip()
    return cleaned


def first_paragraph_text(content: str) -> str:
    paragraph_match = re.search(r"<p[^>]*>(.*?)</p>", content, re.I | re.S)
    if paragraph_match:
        return clean_text(paragraph_match.group(1))
    return clean_text(content)


def excerpt_from_content(content: str, limit: int = 180) -> str:
    text = first_paragraph_text(content)
    if len(text) <= limit:
        return text
    shortened = text[: limit + 1]
    if " " in shortened:
        shortened = shortened.rsplit(" ", 1)[0]
    return shortened.rstrip(".,;:") + "..."


def slug_from_url(url: str) -> str:
    return url.strip("/").split("/")[-1]


def load_current_posts() -> Dict[str, dict]:
    current_entries = json.loads(POSTS_JSON_PATH.read_text("utf-8"))
    return {slug_from_url(entry["url"]): entry for entry in current_entries}


def current_authoritative_slugs() -> set[str]:
    authoritative = set()
    for path in BLOG_ROOT.glob("*/index.html"):
        if path.parent == BLOG_ROOT:
            continue
        contents = path.read_text("utf-8")
        if GENERATED_MARKER not in contents:
            authoritative.add(path.parent.name)
    return authoritative


def wp_image_path_from_attachment(attachment_id: Optional[int], postmeta: Dict[int, Dict[str, List[object]]]) -> Optional[str]:
    if not attachment_id:
        return None
    attachment_meta = postmeta.get(int(attachment_id), {})
    values = attachment_meta.get("_wp_attached_file", [])
    if values:
        return str(values[0])
    return None


def wp_image_alt(attachment_id: Optional[int], postmeta: Dict[int, Dict[str, List[object]]], fallback: str) -> str:
    if not attachment_id:
        return fallback
    attachment_meta = postmeta.get(int(attachment_id), {})
    alt_values = attachment_meta.get("_wp_attachment_image_alt", [])
    if alt_values and str(alt_values[0]).strip():
        return clean_text(str(alt_values[0]))
    return fallback


def copy_featured_image(relative_path: Optional[str]) -> Optional[str]:
    if not relative_path:
        return None

    source = UPLOADS_ROOT / relative_path
    if not source.exists():
        return None

    destination = IMAGE_DEST_ROOT / relative_path
    destination.parent.mkdir(parents=True, exist_ok=True)
    if not destination.exists():
        shutil.copy2(source, destination)

    return "/" + destination.relative_to(ROOT).as_posix()


def rewrite_site_urls(content: str) -> str:
    content = re.sub(
        r'(href|src)="https?://(?:www\.)?salmonhvac\.com(/[^"]*)"',
        lambda match: f'{match.group(1)}="{match.group(2)}"',
        content,
        flags=re.I,
    )
    content = re.sub(
        r'(href|src)="https?://[^"]*/wp-content/uploads/([^"]+)"',
        lambda match: f'{match.group(1)}="/assets/images/blog/wordpress/{match.group(2)}"',
        content,
        flags=re.I,
    )
    content = re.sub(
        r'(href|src)="/wp-content/uploads/([^"]+)"',
        lambda match: f'{match.group(1)}="/assets/images/blog/wordpress/{match.group(2)}"',
        content,
        flags=re.I,
    )
    return content


def human_date(value: str) -> str:
    return datetime.strptime(value[:10], "%Y-%m-%d").strftime("%B %-d, %Y")


def iso_date(value: str) -> str:
    return value[:10]


def estimate_read_time(content: str) -> str:
    word_count = len(re.findall(r"\b[\w'-]+\b", clean_text(content)))
    minutes = max(1, math.ceil(word_count / 225))
    return f"{minutes} min read"


def escape_attr(value: str) -> str:
    return html.escape(value, quote=True)


